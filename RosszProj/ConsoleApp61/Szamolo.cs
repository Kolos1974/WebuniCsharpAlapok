namespace ConsoleApp61
{
   public class Szamolo
    {
        public int Osszad(int a, int b)
        {
            return a + b;
        }

        public int Kivon(int a, int b)
        {
            return a - b;
        }

        public int Szoroz(int a, int b)
        {
            return a * b;
        }

        public int Oszt(int a, int b)
        {
            return a / b;
        }
             
    }
}
